                A soft and squishy all-over cabled scarf, this project is the knit I keep reaching for during these cold winter days. 
                I'm using Knit Picks' <strong><a href="https://www.knitpicks.com/yarn/wool-of-the-andes-worsted/c/5420103?srsltid=AfmBOooAtQS_KFFlhUajL_RDjQMDvEEGNnWq6qDFYN3wvu-exBRRHNQv">Wool of the Andes</a></strong>
                in the 'Baltic Heather' colorway, which is yarn that was gifted to me by my brother for my birthday. It's a lovely blue-violet - hover over the image to see its true color.
                 
                <br/><br/>

                I started this project mid-December, and am only about 1/8<sup>th</sup> of the way done. 
                It's a complicated pattern, but I find it very engaging - by the 3<sup>rd</sup> chart repeat I'm only just starting to need the pattern less frequently. 
                Definitely not a mindless knit!

                <br/><br/>

